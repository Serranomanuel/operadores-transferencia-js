const segundos = 7200; // tiempo en segundos registrado

let minutos = segundos / 60; // 1 minuto = 60 segundos
let horas = minutos / 60; // 1 hora = 60 minutos
let dias = horas / 24; // 1 día = 24 horas

// Mostrar resultados en consola
console.log(`Segundos: ${segundos}`);
console.log(`Minutos: ${minutos}`);
console.log(`Horas: ${horas}`);
console.log(`Días: ${dias}`);

